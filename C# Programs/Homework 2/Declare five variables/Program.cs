using System;
class DeclareFiveVariables
{
    static void Main()
    {
        ushort i = 52130;
        Console.WriteLine(i);
        sbyte k = -115;
        Console.WriteLine(k);
        long t = 4825932;
        Console.WriteLine(t);
        byte l = 97;
        Console.WriteLine(l);
        int p = -10000;
        Console.WriteLine(p);
    }
}