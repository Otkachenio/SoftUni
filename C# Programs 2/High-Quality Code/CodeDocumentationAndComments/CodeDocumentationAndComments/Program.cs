/// <summary>
/// String Extensions
/// </summary>
public static class StringExtensions
{
    /// <summary>
    /// Creats String Bilder from hash table of the input string
    /// </summary>
    /// <param name="input">Input from console</param>
    /// <returns>String Builder of input</returns>
    public static string ToMd5Hash(this string input)
    {
        var md5Hash = MD5.Create();
        var data = md5Hash.ComputeHash(Encoding.UTF8.GetBytes(input));
        var builder = new StringBuilder();

        for (int i = 0; i < data.Length; i++)
        {
            builder.Append(data[i].ToString("x2"));
        }

        return builder.ToString();
    }

    /// <summary>
    /// Check if the string input is true bool value by checking array
    /// with assaigned true boolean values
    /// </summary>
    /// <param name="input">Input from console</param>
    /// <returns>True bool value from input</returns>
    public static bool ToBoolean(this string input)
    {
        var stringTrueValues = new[] { "true", "ok", "yes", "1", "да" };
        return stringTrueValues.Contains(input.ToLower());
    }

    /// <summary>
    /// Checks if the input string can be converted to short number
    /// </summary>
    /// <param name="input">Input from console</param>
    /// <returns>16-bit short number</returns>
    public static short ToShort(this string input)
    {
        short shortValue;
        short.TryParse(input, out shortValue);
        return shortValue;
    }

    /// <summary>
    /// Checks if the string input can be converted to Int32
    /// </summary>
    /// <param name="input">Input from console</param>
    /// <returns>Int32 number</returns>
    public static int ToInteger(this string input)
    {
        int integerValue;
        int.TryParse(input, out integerValue);
